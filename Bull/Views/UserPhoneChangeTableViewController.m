-(IBAction)ok:(id)sender{
    NSString * phone = self.userPhoneTF.text;
    
    if(phone == nil||phone.length <= 0||(![VIewUtil isPhone:phone])){
        [self showAlertWithErrMsg:@"请输入正确的手机号码"];
        return ;
    }
    
    self.user.userPhone = phone;
    if([[UserManger shareInstence]setUser:self.user]){
        [self dismissViewControllerAnimated:YES completion:nil];
    }else{
        [self showAlertWithErrMsg:@"设置失败 ，请检查网络设置。"];
        return;
    }
}

